REPORT_EOF
    
    log_success "Report: $REPORT_FILE"
    cat "$REPORT_FILE"
}

update_memory() {
    log_section "STEP 7: Memory & Learning System Update"
    
    cd "$WORKSPACE"
    
    APK_FILE=$(find out/apk -name "shizuku-*-${BUILD_VARIANT}.apk" -type f | sort -r | head -1)
    APK_SIZE=$(du -h "$APK_FILE" | cut -f1)
    
    # Update memory directories (create if needed)
    mkdir -p "agents/BuildMaster-Shizuku/memory"
    mkdir -p "agents/BuildMaster-Shizuku/agent_info"
    
    # Append to build_baseline.md
    cat >> "agents/BuildMaster-Shizuku/memory/build_baseline.md" << MEMORY_EOF

## Build Run: ${TIMESTAMP}
- **Duration:** ${BUILD_DURATION}s
- **APK Size:** ${APK_SIZE}
- **Status:** SUCCESS
- **Variant:** ${BUILD_VARIANT}
- **Optimization Profile:** production (R8 minification + resource collapse)
- **System:** 2-core Xeon, 2GB RAM, Debian 13

MEMORY_EOF
    
    log_success "Memory: build_baseline.md updated"
    
    # CSV performance log
    echo "${TIMESTAMP},${BUILD_DURATION},${APK_SIZE},SUCCESS,${BUILD_VARIANT}" >> \
        "agents/BuildMaster-Shizuku/memory/performance_log.csv"
    
    log_success "Metrics: performance_log.csv updated"
}

git_commit() {
    log_section "STEP 8: Git Commit & Push"
    
    cd "$WORKSPACE"
    
    APK_FILE=$(find out/apk -name "shizuku-*-${BUILD_VARIANT}.apk" -type f | sort -r | head -1)
    APK_SIZE=$(du -h "$APK_FILE" | cut -f1)
    
    # Stage artifacts
    git add -A || true
    
    # Commit with structured message
    COMMIT_MSG="build(Shizuku): ${BUILD_VARIANT^} APK ${TIMESTAMP} — ${APK_SIZE} | ${BUILD_DURATION}s

Agent: $AGENT_NAME
Task: $TASK_ID
Status: SUCCESS
Variant: $BUILD_VARIANT
Optimization: R8 minification + resource collapse
System: 2-core Xeon, 2GB RAM, Debian 13

Metrics:
  duration_sec: ${BUILD_DURATION}
  apk_size: ${APK_SIZE}
  variant: ${BUILD_VARIANT}"
    
    git commit -m "$COMMIT_MSG" || log_warn "Nothing to commit (clean working tree)"
    
    # Create tag
    TAG="v13.6.0-build-${TIMESTAMP}"
    git tag -a "$TAG" -m "Shizuku 13.6.0 $BUILD_VARIANT build — ${APK_SIZE} — ${BUILD_DURATION}s" || log_warn "Tag $TAG already exists"
    
    log_success "Committed: $TAG"
    
    # Push (non-blocking if network down)
    if git push origin main 2>&1 | tee /tmp/git_push.log; then
        log_success "Pushed to origin/main"
    else
        log_warn "Git push failed (network issue?), will retry next cycle"
    fi
    
    if git push origin --tags 2>&1 | tee /tmp/git_push_tags.log; then
        log_success "Pushed tags"
    else
        log_warn "Git push tags failed"
    fi
}

post_completion() {
    log_section "STEP 9: Post-Completion Notification"
    
    cd "$WORKSPACE"
    
    APK_FILE=$(find out/apk -name "shizuku-*-${BUILD_VARIANT}.apk" -type f | sort -r | head -1)
    APK_SIZE=$(du -h "$APK_FILE" | cut -f1)
    
    # Create inter-agent message
    mkdir -p message_system
    
    cat > "message_system/Agent-BuildMaster-to-TestRunner-${TIMESTAMP}.txt" << MSG_EOF
FROM: BuildMaster-Shizuku
TO: TestRunner-APK
TIMESTAMP: $(date -u +%Y-%m-%dT%H:%M:%SZ)
TASK_ID: $TASK_ID
STATUS: SUCCESS

APK build successful. Artifact ready for testing.

Artifact Details:
- Path: $APK_FILE
- Size: ${APK_SIZE}
- Variant: ${BUILD_VARIANT}
- Duration: ${BUILD_DURATION}s
- Signature: Valid
- Min API: 24, Target API: 36

$(if [ "$BUILD_VARIANT" = "release" ]; then
echo "ProGuard Mapping: Available (out/mapping/)"
fi)

Next Steps:
- Install on test device(s)
- Run functional tests
- Report results

Expected Test Duration: 15 minutes
Urgency: Non-blocking

MSG_EOF
    
    log_success "Inter-agent message queued"
    
    # Log to completion folder
    mkdir -p "tasks/complete"
    echo "Task $TASK_ID completed at $(date -u +%Y-%m-%dT%H:%M:%SZ)" >> "tasks/complete/BUILD_${BUILD_VARIANT^^}_COMPLETE.log"
    
    log_success "Completion logged"
}

# ===== MAIN WORKFLOW =====

main() {
    echo ""
    echo "╔════════════════════════════════════════════════════════════╗"
    echo "║        BuildMaster-Shizuku Autonomous Workflow             ║"
    echo "║                      AIOS Agent v1.0                       ║"
    echo "╚════════════════════════════════════════════════════════════╝"
    echo ""
    echo "Agent: $AGENT_NAME"
    echo "Task: $TASK_ID"
    echo "Variant: $BUILD_VARIANT"
    echo "Timestamp: $TIMESTAMP"
    echo ""
    
    # Execute workflow steps
    validate_environment
    echo ""
    
    system_baseline
    echo ""
    
    prepare_gradle
    echo ""
    
    build_apk
    echo ""
    
    validate_apk
    echo ""
    
    generate_report
    echo ""
    
    update_memory
    echo ""
    
    git_commit
    echo ""
    
