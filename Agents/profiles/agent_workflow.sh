    log_section "STEP 6: Report Generation"
    
    cd "$WORKSPACE"
    
    APK_FILE=$(find out/apk -name "shizuku-*-${BUILD_VARIANT}.apk" -type f | sort -r | head -1)
    APK_SIZE=$(du -h "$APK_FILE" | cut -f1)
    MAPPING_FILE=$(find out/mapping -name "mapping-*.txt" -type f | sort -r | head -1)
    
