#!/bin/bash
# agent_workflow.sh — BuildMaster-Shizuku Autonomous Workflow
# Purpose: Execute complete APK build cycle with memory updates, reporting, and git push
# AIOS Integration: Full self-improving agent with task tracking
# Status: PRODUCTION READY
# Usage: ./agent_workflow.sh [debug|release]

set -e  # Exit on first error

# ===== CONSTANTS & ENVIRONMENT =====
WORKSPACE="${WORKSPACE:-/workspace/Shizuku-13.6.0}"
AGENT_NAME="BuildMaster-Shizuku"
TASK_ID="build_shizuku_release_v1_20260702"
TIMESTAMP=$(date -u +%Y%m%d_%H%M%S)
BUILD_VARIANT="${1:-release}"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Export Android environment
export ANDROID_SDK_ROOT="${ANDROID_SDK_ROOT:-/android/sdk}"
export ANDROID_NDK_ROOT="${ANDROID_NDK_ROOT:-/android/ndk/29.0.13113456}"
export JAVA_HOME="${JAVA_HOME:-/usr/lib/jvm/java-21-openjdk-amd64}"
export PATH="$JAVA_HOME/bin:$PATH"

# Color output for readability
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'  # No Color

# ===== FUNCTIONS =====

log_section() {
    echo -e "${BLUE}[$(date -u +%H:%M:%S)] ${AGENT_NAME}: $1${NC}"
}

log_success() {
    echo -e "${GREEN}✓ $1${NC}"
}

log_warn() {
    echo -e "${YELLOW}⚠ $1${NC}"
}

log_error() {
    echo -e "${RED}✗ $1${NC}"
}

validate_environment() {
    log_section "STEP 1: Environment Validation"
    
    if [ ! -d "$WORKSPACE" ]; then
        log_error "Workspace not found: $WORKSPACE"
        exit 1
    fi
    
    if ! command -v java &> /dev/null; then
        log_error "Java 21 not found in PATH"
        exit 1
    fi
    
    JAVA_VERSION=$(java -version 2>&1 | grep -oE '"[0-9_]+' | head -1 | cut -d'"' -f2)
    if [[ ! $JAVA_VERSION == *"21"* ]]; then
        log_warn "Java version is $JAVA_VERSION (expected 21)"
    fi
    log_success "Java: $JAVA_VERSION"
    
    if [ ! -f "$WORKSPACE/gradlew" ]; then
        log_error "gradlew not found in $WORKSPACE"
        exit 1
    fi
    log_success "Gradle wrapper found"
    
    if [ ! -d "$ANDROID_SDK_ROOT" ]; then
        log_warn "ANDROID_SDK_ROOT not found at $ANDROID_SDK_ROOT (set correctly if needed)"
    else
        log_success "Android SDK: $ANDROID_SDK_ROOT"
    fi
    
    log_success "Environment validated"
}

system_baseline() {
    log_section "STEP 2: System Baseline Audit"
    
    AUDIT_FILE="$WORKSPACE/system_audit_${TIMESTAMP}.md"
    
    cat > "$AUDIT_FILE" << EOF
# System Baseline - $TIMESTAMP
Generated by: $AGENT_NAME

## CPU
\`\`\`
$(lscpu)
\`\`\`

## Memory
\`\`\`
$(free -h)
\`\`\`

## Disk
\`\`\`
$(df -h /)
\`\`\`

## Java
\`\`\`
$(java -version 2>&1)
\`\`\`

## Gradle
\`\`\`
$($WORKSPACE/gradlew --version 2>&1 | head -5)
\`\`\`

EOF
    
    log_success "System audit: $AUDIT_FILE"
}

prepare_gradle() {
    log_section "STEP 3: Gradle Configuration"
    
    cd "$WORKSPACE"
    
    # Backup existing gradle.properties
    if [ -f "gradle.properties" ]; then
        cp gradle.properties gradle.properties.bak_${TIMESTAMP}
        log_success "Backed up gradle.properties"
    fi
    
    # Deploy optimized gradle.properties for 2-core/2GB system
    cat > gradle.properties << 'GRADLE_PROPS'
# BuildMaster-Shizuku Optimized Configuration
# System: 2-core Xeon @ 2.6GHz, 2GB RAM, Debian 13
# Generated: Automated by AIOS agent

# JVM Tuning: Heap capping + G1GC for 2GB system
org.gradle.jvmargs=-Xmx1200m -Xms512m -XX:+UseG1GC -XX:G1HeapRegionSize=8m -XX:+ParallelRefProcEnabled -XX:+UnlockDiagnosticVMOptions -XX:G1SummarizeRSetStatsPeriod=20000

# Build Performance: 2-worker limit (2 cores)
org.gradle.workers.max=2
org.gradle.parallel=true
org.gradle.configureondemand=true

# Caching
org.gradle.caching=true
org.gradle.build.cache.local.enabled=true

# Android SDK Optimization
android.enableProfileInstaller=false
android.enableVcsInfo=false
android.aapt2PoolingStandalone=false

# Kotlin Daemon
kotlin.daemon.jvm.options=-Xmx800m

# Logging
org.gradle.warning.mode=summary

GRADLE_PROPS
    
    log_success "gradle.properties deployed (optimized for 2-core/2GB)"
    
    # Verify settings
    ./gradlew properties 2>&1 | grep -E "org.gradle.jvmargs|org.gradle.workers.max|org.gradle.parallel" | head -3
    log_success "Gradle configuration verified"
}

build_apk() {
    log_section "STEP 4: APK Build [$BUILD_VARIANT variant]"
    
    cd "$WORKSPACE"
    
    BUILD_LOG="/tmp/build_${TIMESTAMP}.log"
    BUILD_START=$(date +%s)
    
    echo "[$(date -u +%H:%M:%S)] Starting $BUILD_VARIANT build..." | tee "$BUILD_LOG"
    
    case "$BUILD_VARIANT" in
        debug)
            ./gradlew :manager:assembleDebug --profile 2>&1 | tee -a "$BUILD_LOG" || {
                log_error "Debug build failed. See $BUILD_LOG"
                tail -50 "$BUILD_LOG"
                exit 1
            }
            ;;
        release)
            ./gradlew :manager:assembleRelease --profile 2>&1 | tee -a "$BUILD_LOG" || {
                log_error "Release build failed. See $BUILD_LOG"
                tail -50 "$BUILD_LOG"
                exit 1
            }
            ;;
        *)
            log_error "Unknown variant: $BUILD_VARIANT"
            exit 1
            ;;
    esac
    
    BUILD_END=$(date +%s)
    BUILD_DURATION=$((BUILD_END - BUILD_START))
    
    log_success "Build completed in ${BUILD_DURATION}s"
}

validate_apk() {
    log_section "STEP 5: APK Validation"
    
    cd "$WORKSPACE"
    
    APK_FILE=$(find out/apk -name "shizuku-*-${BUILD_VARIANT}.apk" -type f | sort -r | head -1)
    
    if [ -z "$APK_FILE" ] || [ ! -f "$APK_FILE" ]; then
        log_error "APK not found in out/apk/ for variant: $BUILD_VARIANT"
        exit 1
    fi
    
    log_success "APK located: $APK_FILE"
    
    # Size
    APK_SIZE=$(du -h "$APK_FILE" | cut -f1)
    APK_SIZE_BYTES=$(stat -c%s "$APK_FILE")
    APK_SIZE_MB=$((APK_SIZE_BYTES / 1048576))
    
    log_success "APK size: ${APK_SIZE} (${APK_SIZE_MB} MB)"
    
    # AAPT badging
    if ! aapt dump badging "$APK_FILE" > /dev/null 2>&1; then
        log_error "AAPT badging failed"
        exit 1
    fi
    log_success "AAPT badging: OK"
    
    # Signature verification
    if ! jarsigner -verify "$APK_FILE" > /dev/null 2>&1; then
        log_warn "Signature verification warning (may be expected for debug builds)"
    else
        log_success "APK signature: Valid"
    fi
    
    # Store APK info for later
    APK_INFO_FILE="/tmp/apk_info_${TIMESTAMP}.txt"
    cat > "$APK_INFO_FILE" << EOF
APK_FILE=$APK_FILE
APK_SIZE=$APK_SIZE
APK_SIZE_MB=$APK_SIZE_MB
BUILD_VARIANT=$BUILD_VARIANT
BUILD_DURATION=$BUILD_DURATION
TIMESTAMP=$TIMESTAMP
EOF
    
    log_success "APK validation complete"
}

